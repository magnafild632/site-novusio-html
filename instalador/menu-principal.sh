# Fun√ß√£o para configurar .env
configure_env() {
    print_title
    echo -e "${YELLOW}‚öôÔ∏è CONFIGURAR .ENV${NC}"
    echo ""
    
    # Verificar se j√° existe .env
    if [[ -f "/opt/novusio/.env" ]]; then
